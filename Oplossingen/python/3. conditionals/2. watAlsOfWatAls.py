# Als het getal lager is dan 5, print dan het woord "Laag!"
# Als het getal hoger is dan 10, print het woord "Hoog!"
# Als het getal niet lager is dan 5 of hoger dan 10, print het woord "Midden!"

for i in range(0, 15):
    if i <= 5:
        print("Laag!")
    elif i >= 10:
        print("Hoog!")
    else:
        print("Midden!")
